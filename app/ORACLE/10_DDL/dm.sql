  CREATE OR REPLACE FORCE VIEW "LUDZIE_KALENDARZ" ("LUDZIE_ID", "DZIEN", "L", "TYP", "PRACOWNIK") AS
   select
		  r.id LUDZIE_ID,
		  r.dzien,
		  r.l,
		  decode(r.L4, r.l ,'L4', decode(r.U, r.l, 'U', decode(r.PP, r.l, 'PP', 'P')))  typ,
		  r.imie ||' '|| r.nazwisko pracownik
		from
		  (
			select
			  substr(trim(l.ZWOLNIENIA_LEKARSKIE),0,1) L4,
			  substr(trim(l.GRAFIK_URLOPOW), 0,1 ) U,
			  substr(trim(l.GRAFIK_POGOTOWIA_DOMOWEGO), 0,1 ) PP,
			  l.*,
			  d.*
			from
			  ludzie l,
			  (
				select
				 to_date(to_char(nvl((select max(IMP_END) from IMP_FILE),sysdate),'YYYYMMDD'))  + level dzien,
				  to_char(rownum) l
				from
				  dual
				  where to_char(to_date(to_char(nvl((select max(IMP_END) from IMP_FILE),sysdate),'YYYYMMDD'))  + level, 'DY', 'NLS_DATE_LANGUAGE=ENGLISH')
				 not in ('SAT','SUN')
				  connect by level < 100
			  )
			  d
		  )
		  r;
